public class WaitingTime {
    int startW ;
    int endW ;

    public WaitingTime(int startW, int endW) {
        this.startW = startW;
        this.endW = endW;
    }

    public int getStartW() {
        return startW;
    }

    public void setStartW(int startW) {
        this.startW = startW;
    }

    public int getEndW() {
        return endW;
    }

    public void setEndW(int endW) {
        this.endW = endW;
    }
 
    
    
}
